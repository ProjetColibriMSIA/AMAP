<?php

namespace AMAPBundle\Entity\WebSurfer;

use Doctrine\ORM\Mapping as ORM;

/**
 * SignUp
 *
 * @ORM\Table(name="sign_up")
 * @ORM\Entity(repositoryClass="AMAPBundle\Repository\WebSurfer\SignUpRepository")
 */
class SignUp {

    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * Get id
     *
     * @return int
     */
    public function getId() {
        return $this->id;
    }

}
