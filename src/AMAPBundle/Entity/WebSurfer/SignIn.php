<?php

namespace AMAPBundle\Entity\WebSurfer;

use Doctrine\ORM\Mapping as ORM;

/**
 * SignIn
 *
 * @ORM\Table(name="sign_in")
 * @ORM\Entity(repositoryClass="AMAPBundle\Repository\WebSurfer\SignInRepository")
 */
class SignIn
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }
}
